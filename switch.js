var numero = 1;

switch (numero) {
  case 1:
    console.log("Soy uno!");
    break; //si se cumple lo primero aqui se termina el ciclo.
  case 2:
    console.log("Soy un dos!");
    break;
  case 3:
    console.log("Soy un 3!");
    break;
  default: //si nada de lo anterior se cumple entra en default
    console.log("No soy nada");      
}